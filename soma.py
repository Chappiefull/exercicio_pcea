soma = lambda n1, n2: n1 + n2 #Função que faz a soma entre dois números
sub = lambda n1, n2: n1 - n2 #Função que faz a subtração entre dois números
mult = lambda n1, n2: n1 * n2 #Função que faz a multiplicação entre dois números
div = lambda n1, n2: n1 / n2 #Função que faz a divisão entre dois números
pot = lambda n1, n2: n1 ** n2 #Função que faz a potência entre dois números (n1 é a base e n2 é o expoente)
raiz = lambda n1, n2: n1 ** (1/n2) #Função que faz a radiciação (n1 é o radicando e n2 é o índice)
